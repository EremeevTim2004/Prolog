% вычисление значения Y
expression1(X, Y) :- 
    X \= 2, % избегаем деления на 0
    Y is (X*X + 1) / (X - 2).

% вычисление значения S
expression2(X, Y, S) :- 
    X + Y \= 0, % избегаем деления на 0
    S is 2 * ((X*X) + (Y*Y)) / (X + Y).

% вычисление среднего арифметического
average(X, Y, Avg) :- 
    Avg is (X + Y) / 2.

% проверка четности числа
is_even(N) :- 
    N mod 2 =:= 0.

% проверка попадания X в диапазон [A, B]
in_range(X, A, B) :- 
    X >= A, 
    X =< B.

% нахождение минимального числа среди трех
min_of_three(X, Y, Z, Min) :- 
    Min is min(X, min(Y, Z)).

% нахождение максимального числа среди трех
max_of_three(X, Y, Z, Max) :- 
    Max is max(X, max(Y, Z)).
