% Определяем друзей
друг(белокуров).
друг(рыжов).
друг(чернов).

% Определяем возможные цвета волос
цвет_волос(блондин).
цвет_волос(рыжий).
цвет_волос(брюнет).

% Правила, запрещающие совпадение цвета волос с фамилией
не_совпадает(белокуров, блондин).
не_совпадает(рыжов, рыжий).
не_совпадает(чернов, брюнет).

% Определяем соответствие между друзьями и цветами волос
соответствие(Друг, Цвет) :- друг(Друг), цвет_волос(Цвет), \+ не_совпадает(Друг, Цвет).

% Правило для решения задачи
решение(X1, Y1, X2, Y2, X3, Y3) :-
    соответствие(X1, Y1), % Белокуров
    соответствие(X2, Y2), % Рыжов
    соответствие(X3, Y3), % Чернов
    X1 = белокуров, X2 = рыжов, X3 = чернов,
    Y1 \== Y2, Y2 \== Y3, Y1 \== Y3. % все должны иметь разные цвета волос
